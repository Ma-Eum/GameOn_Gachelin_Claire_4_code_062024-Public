/* fonction permettant de gérer l'apparition et la
disparition du mode menu hamburger
*/ 
function editNav() {
  var x = document.getElementById("myTopnav");
  if (x.className === "topnav") {
    x.className += " responsive";
  } else {
    x.className = "topnav";
  }
}


